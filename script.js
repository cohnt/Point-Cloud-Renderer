///////////////////////////////////////////
/// CONSTANTS
///////////////////////////////////////////

var canvasWidth = 750;
var canvasHeight = 750;
var canvasId = "display";

///////////////////////////////////////////
/// GLOBAL VARIABLES
///////////////////////////////////////////

var html = {};

///////////////////////////////////////////
/// CLASSES
///////////////////////////////////////////



///////////////////////////////////////////
/// FUNCTIONS
///////////////////////////////////////////

function setup() {
	html.canvas = document.getElementById(canvasId);

	html.canvas.width = String(canvasWidth) + "px";
	html.canvas.height = String(canvasHeight) + "px";
}

///////////////////////////////////////////
/// EXECUTED CODE
///////////////////////////////////////////

setup();